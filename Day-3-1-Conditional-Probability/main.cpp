#include <iostream>

using namespace std;

int main() {

    // Given that we know one child is a boy,
    // we have the following sample space:
    // (Child1=Boy, Child2=Girl) (Child1=Girl, Child2=Boy) (Child1=Boy, Child2=Boy)
    // Thus the probability of both children being boys is 1/3.
    cout << "1/3" << endl;

    return 0;
}